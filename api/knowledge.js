const tokenize = (t) =>
  t
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, " ")
    .split(/\s+/)
    .filter(Boolean);

export function retrieveContext(query, topK = 5) {
  const q = query.toLowerCase();
  const qTokens = tokenize(query);

  const isHireQuery = /hire|recruit|why yash|should i|strong|best candidate|fit/.test(q);
  const isTeslaQuery = /tesla/.test(q);
  const isResearchQuery = /research|paper|publication|ieee|scopus|xai|yolo|ar therapy/.test(q);
  const isProjectQuery = /project|github|built|aethermart|cmis|analyzer|airline|agent/.test(q);
  const isContactQuery = /contact|email|phone|linkedin|reach|connect/.test(q);

  const scored = profileDocs.map((doc) => {
    const text = `${doc.title} ${doc.content}`.toLowerCase();
    const tokenScore = qTokens.reduce((a, tok) => a + (text.includes(tok) ? 1 : 0), 0);

    let boost = 0;
    if (isHireQuery && /why-hire|experience|skills|education/.test(doc.id)) boost += 5;
    if (isTeslaQuery && doc.id === "experience-tesla") boost += 6;
    if (isResearchQuery && doc.id.startsWith("research")) boost += 4;
    if (isProjectQuery && doc.id.startsWith("project")) boost += 3;
    if (isContactQuery && doc.id === "contact") boost += 6;

    return { doc, score: tokenScore + boost };
  });

  const results = scored
    .filter((x) => x.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, topK)
    .map((x) => x.doc);

  return results.length ? results : profileDocs.slice(0, 4);
}
